    private void cmbPesoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbPesoActionPerformed
        JComboBox cb = (JComboBox)evt.getSource();
        String opcionGota = (String) cb.getSelectedItem();
        
        if (opcionGota.contentEquals(libra))
            conversionPeso = 1;
        else if (opcionGota.contentEquals(kilo))
             conversionPeso = 0;
    }//GEN-LAST:event_cmbPesoActionPerformed

    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        Peso = Double.parseDouble(txtPeso.getText());
       
        if(conversionPeso == 1){
            total = Peso * (1 / 2.20462);
        }
        else if(conversionPeso == 0){
            total = Peso * (2.20462 / 1);
        } 
        
        txtTotal.setText(String.valueOf(total));
    }//GEN-LAST:event_btnCalcularActionPerformed


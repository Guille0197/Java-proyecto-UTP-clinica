    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        Longitud = Double.parseDouble(txtLongitud.getText());
        
        if(conversionLongitud == 1){
            total = Longitud * (1 / 3.28084);
        }
        else if(conversionLongitud == 0){
            total = Longitud *  (3.28084 / 1);
        } 
        
        txtTotal.setText(String.valueOf(total));
        
    }//GEN-LAST:event_btnCalcularActionPerformed

    private void cmbLongitudActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbLongitudActionPerformed
        JComboBox cb = (JComboBox)evt.getSource();
        String opcionLongitud = (String) cb.getSelectedItem();
        
        if (opcionLongitud.contentEquals(pies))
            conversionLongitud = 1;
        else if (opcionLongitud.contentEquals(metros))
             conversionLongitud = 0;
    }//GEN-LAST:event_cmbLongitudActionPerformed

